/// # The Fuzz Lobe
///
/// As recommended by the OpenPBR specification, and following the official reference
/// implementation and the Adobe implementation, it's an implementation of the model proposed
/// by the paper "Practical Multiple-Scattering Sheen Using Linearly Transformed Cosines"
/// by Zeltner, Burley, and Chiang (2022).
pub struct Fuzz {
    pub color: Vec3,
    pub roughness: f32,
}

impl From<&Material> for Fuzz {
    fn from(m: &Material) -> Self {
        Self {
            color: m.fuzz_color,
            roughness: m.fuzz_roughness,
        }
    }
}

fn phi(v: Vec3) -> f32 {
    let p = f32::atan2(v.y, v.x);
    if p < 0.0 {
        p + 2.0 * PI
    } else {
        p
    }
}

impl Lobe for Fuzz {
    fn wo_is_valid(&self, wo: Vec3) -> bool {
        wo.is_in_upper_hemisphere()
    }

    fn eval(&self, wo: Vec3, wi: Vec3) -> Throughput {
        if !wi.is_in_upper_hemisphere() || !wo.is_in_upper_hemisphere() {
            return Throughput::ZERO;
        }

        let wi_rotated = wi.rotate_axis(Vec3::Z, -phi(wo));
        let wo_rotated = wo.rotate_axis(Vec3::Z, -phi(wi));

        let coeffs_wo = ltc_coeffs(wo, self.roughness);
        let coeffs_wi = ltc_coeffs(wi, self.roughness);

        // The cosine term is included in the LTC density, so we divide it back out to get the BRDF.
        let f1 = ltc(wi_rotated, coeffs_wo) * coeffs_wo[2] / wi.cos_theta();
        let f2 = ltc(wo_rotated, coeffs_wi) * coeffs_wi[2] / wo.cos_theta();

        Throughput::from_diffuse((f1 * f2).sqrt() * self.color)
    }

    fn sample(&self, random: Vec3, wo: Vec3) -> Option<Sample> {
        if !wo.is_in_upper_hemisphere() {
            return None;
        }

        let wi = sample_ltc(ltc_coeffs(wo, self.roughness), random.truncate())
            .rotate_axis(Vec3::Z, phi(wo));

        if !wi.is_in_same_hemisphere(&wo) {
            return None;
        }

        let throughput = self.eval(wo, wi);
        let density = self.density(wo, wi);

        Some(Sample {
            lobe_type: LobeType::Fuzz,
            throughput,
            density,
            wi,
        })
    }

    fn density(&self, wo: Vec3, wi: Vec3) -> f32 {
        if !wi.is_in_upper_hemisphere() || !wo.is_in_upper_hemisphere() {
            return 0.0;
        }

        let wi = wi.rotate_axis(Vec3::Z, -phi(wo));
        ltc(wi, ltc_coeffs(wo, self.roughness))
    }
}

fn ltc(wi: Vec3, coeffs: Vec3) -> f32 {
    let (a_inv, b_inv) = (coeffs[0], coeffs[1]);
    let mut original_wi = Vec3::new(a_inv * wi.x + b_inv * wi.z, a_inv * wi.y, wi.z);

    let length = original_wi.length();
    original_wi /= length;

    let jacobian = (a_inv * a_inv) / length.powi(3);
    sampling::cosine_hemisphere_density(original_wi.cos_theta()) * jacobian
}

fn sample_ltc(coeffs: Vec3, random: Vec2) -> Vec3 {
    let wi = sampling::cosine_hemisphere_sample(random);
    let (a_inv, b_inv) = (coeffs[0], coeffs[1]);
    Vec3::new(wi.x / a_inv - wi.z * b_inv / a_inv, wi.y / a_inv, wi.z).normalize()
}

fn ltc_coeffs(wo: Vec3, roughness: f32) -> Vec3 {
    let row = f32::max(0.0, roughness.min(1.0f32.next_down())) * (LTC_RESOLUTION - 1) as f32;
    let col = f32::max(0.0, wo.cos_theta().min(1.0f32.next_down())) * (LTC_RESOLUTION - 1) as f32;

    let (row_floor, col_floor) = (row.floor(), col.floor());
    let (row_frac, col_frac) = (row - row_floor, col - col_floor);
    let (row_index, col_index) = (row_floor as usize, col_floor as usize);

    (LTC_VOLUME[row_index][col_index] * (1.0 - col_frac)
        + LTC_VOLUME[row_index][col_index + 1] * col_frac)
        * (1.0 - row_frac)
        + (LTC_VOLUME[row_index + 1][col_index] * (1.0 - col_frac)
            + LTC_VOLUME[row_index + 1][col_index + 1] * col_frac)
            * row_frac
}

